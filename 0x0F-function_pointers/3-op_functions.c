#include "3-calc.h"

/**
 * op_add - adds two numbers
 * @a: input 1
 * @b: input 2
 * Return: result
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two numbers
 * @a: input 1
 * @b: input 2
 * Return: result
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op mul - multiply two numbers
 * @a: input 1
 * @b: input 2
 * Return: result
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devide two numbers
 * @a: input 1
 * @b: input 2
 * Return: result
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - determine the reminder
 * @a: input 1
 * @b: input 2
 * Return: result
*/

int op_mod(int a, int b)
{
	return (a % b);
}
